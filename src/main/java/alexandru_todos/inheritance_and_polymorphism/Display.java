package main.java.alexandru_todos.inheritance_and_polymorphism;

// (Overload): Create a ‘Display’ class and overloads the show method to display an int, a double and a String.
public class Display {
    public int show(int x) {
        return x;
    }

    public double show(double y) {
        return y;
    }

    public String show(String z) {
        return z;
    }
}
