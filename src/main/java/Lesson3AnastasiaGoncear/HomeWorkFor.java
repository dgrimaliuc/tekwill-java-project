package Lesson3AnastasiaGoncear;
//Calculate the sum of numbers from 1 to 100 using a for loop.
public class HomeWorkFor {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
                sum += i;
            }

        System.out.println("The sum of all numbers from 1 to 100 is: " + sum);
    }
}

