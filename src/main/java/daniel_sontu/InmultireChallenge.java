package main.java.daniel_sontu;

public class InmultireChallenge {
    public static void main(String[] args) {
        int numar = 6;

        System.out.println("Tabla înmulțirii pentru numărul " + numar + ":");

        for (int i = 1; i <= 10; i++) {
            System.out.println(numar + " * " + i + " = " + (numar * i));
        }
    }
}
