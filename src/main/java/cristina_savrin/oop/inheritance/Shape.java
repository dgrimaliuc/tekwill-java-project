package cristina_savrin.oop.inheritance;

/**
 * Create a class named "Shape" with instance variables for color.
 */

public class Shape {
    private String color;

    public Shape(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
