package main.java.Magda_Petrachi;

public class whileCycle {
    public static void main(String[] args) {
        int i = 55;
        int sum = 0;
        while (i>= 111) {
            sum += i;
            i++;
        }
        System.out.println("Suma numerilor:" + sum);
    }
}

