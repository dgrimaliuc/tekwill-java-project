package main.java.denisGrimaliuc.inheritance;

/**
 * Implementați o clasă numită „Animal” cu variabile de instanță pentru nume, vârstă, tip și sunet.
 */
public class Animal {
    String name;
    int age;
    String type;
    String sound;

    Animal(String name, int age, String type, String sound) {
        this.name = name;
        this.age = age;
        this.type = type;
        this.sound = sound;
    }
}
