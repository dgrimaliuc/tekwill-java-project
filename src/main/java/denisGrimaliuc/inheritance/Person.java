package main.java.denisGrimaliuc.inheritance;

/**
 * Dezvoltați o clasă numită „Person” cu variabile de instanță pentru nume, vârstă și adresă.
 */
public class Person {
    String name;
    int age;
    String address;

    Person(String name, int age, String address) {
        this.name = name;
        this.age = age;
        this.address = address;
    }
}
