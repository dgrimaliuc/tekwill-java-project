package main.java.denisGrimaliuc.inheritance;


/**
 * Creați o clasă numită „Vehicle” cu variabile de instanță pentru marcă, culoare și an.
 */
public class Vehicle {
    String brand;
    String color;
    int year;

    Vehicle(String brand, String color, int year) {
        this.brand = brand;
        this.color = color;
        this.year = year;
    }

}
