package LiubovZapsa.Lesson1707;

public class Homework3 {

    public static void printNumbers(int x) {
        for (int i = 1; i <= x; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
    }

    public static void main(String[] args) {

        printNumbers(12);
    }
}
